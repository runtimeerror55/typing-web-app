export const HomePage = () => {
      // loaders return values
      const loaderData = useAsyncValue();
      const statsData = useAsyncValue()[1].value;
      let settingsData = useAsyncValue()[2].value;

      if (settingsData.status === "error") {
            settingsData = { payload: { settings: defaultSettings } };
      }

      const [allWords, setAllWords] = useState(commonWords);
      const [wordIndex, setWordIndex] = useState(0);
      const [timer, setTimer] = useState(settingsData.payload.settings.timer);
      const [mode, setMode] = useState("test");
      const [modeOne, setModeOne] = useState("words");
      const [modeTwo, setModeTwo] = useState("0");
      const [typingSoundPath, setTypingSoundPath] = useState(flastTwo);
      const [theme, setTheme] = useState(settingsData.payload.settings.theme);

      const typingSound = new Howl({
            src: [typingSoundPath],
      });

      const restartButtonRef = useRef();
      const pageKeyDownHandler = (event) => {
            event.preventDefault();
            event.stopPropagation();
            if (event.key === "Tab") {
                  restartButtonRef.current.focus();
            }
      };

      if (loaderData.status === "error") {
            return (
                  <div
                        className={
                              styles["page"] +
                              " " +
                              styles[`home-page-green-theme`]
                        }
                        onKeyDown={pageKeyDownHandler}
                        tabIndex={0}
                  >
                        {loaderData.message}
                  </div>
            );
      } else {
            return (
                  <div
                        className={
                              styles["page"] +
                              " " +
                              styles[`home-page-${theme}`]
                        }
                        onKeyDown={pageKeyDownHandler}
                        tabIndex={0}
                  >
                        <ToastContainer />
                        <NavBar></NavBar>
                        <main className={styles["main"]}>
                              {mode === "practise" ? (
                                    <PracticeWord
                                          theme={theme}
                                          statsData={statsData}
                                          wordIndex={wordIndex}
                                          allWords={allWords}
                                    ></PracticeWord>
                              ) : null}

                              <section className={styles["typing-section"]}>
                                    <TypingArea
                                          ref={restartButtonRef}
                                          timer={timer}
                                          typingSound={typingSound}
                                          theme={theme}
                                          allWords={allWords}
                                          mode={mode}
                                          setWordIndex={setWordIndex}
                                          wordIndex={wordIndex}
                                          key={mode + modeOne + modeTwo}
                                          modeOne={modeOne}
                                          modeTwo={modeTwo}
                                          allLetters={allLetters}
                                    ></TypingArea>
                              </section>
                              <QuickSettings
                                    setTimer={setTimer}
                                    setTypingSoundPath={setTypingSoundPath}
                                    settings={settingsData.payload.settings}
                                    setTheme={setTheme}
                                    theme={theme}
                                    setMode={setMode}
                                    setModeOne={setModeOne}
                                    mode={mode}
                                    modeOne={modeOne}
                                    setAllWords={setAllWords}
                                    modeTwo={modeTwo}
                                    setModeTwo={setModeTwo}
                                    allLetters={allLetters}
                                    allWords={commonWords}
                                    setWordIndex={setWordIndex}
                              ></QuickSettings>
                              {/* <aside className={styles["practise-words"]}>
                                    {allWords.map((word, wordIndex) => {
                                          return (
                                                <PracticeWord
                                                      theme={theme}
                                                      statsData={statsData}
                                                      wordIndex={wordIndex}
                                                      allWords={allWords}
                                                ></PracticeWord>
                                          );
                                    })}
                              </aside> */}
                        </main>
                  </div>
            );
      }
};
